public class Faktoriyel {
    public static void main(String[] args) {
        // Faktöriyel hesabı için başlangıç değeri
        int sayi = 5;
        // 5! = 5 × 4 × 3 × 2 × 1
        int sonuc = sayi * 4 * 3 * 2 * 1;
        // Sonucu ekrana yazdır
        System.out.println("5! = " + sonuc);
    }
}
